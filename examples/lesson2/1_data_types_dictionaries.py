# Define an initial dictionary
my_dict = {
  'one': 1,
  'two': 2,
  'three': 3,
}

# Add a new number to the end of the dictionary
my_dict['four'] = 4

# Print out the dictionary
print(my_dict)

# Print the value 'one' of the dictionary
print(my_dict['one'])

# Change a value on the dictionary
my_dict['one'] = 5

# Print out the altered dictionary
print(my_dict)
